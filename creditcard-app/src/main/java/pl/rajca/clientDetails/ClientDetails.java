package pl.rajca.clientDetails;

class ClientDetails {
    private Integer id;
    private String name;
    private String surname;
    
    ClientDetails(){}
    
    public Integer getId(){
        return id;
    }
    
    public void setId(){
        id = id;
    }
    
    public String getName() {
        return getName;
    }
    
    public void setName(){
        name = name;
    }
    
    public void setSurname(){
        surname = surname;
    }
    
    public String getSurname(){
        return surname;
    }
}